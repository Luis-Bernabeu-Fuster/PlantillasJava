public class Ejemplo3 {
    public static void main(String[] args) {
        //muestra los número con salto de 2
        for (int i = 1; i <= 10; i+=2) {
            System.out.print(" "+i);
        }

        //muestra los número del 10 al 1
        for (int i = 10; i >= 1; i--) {
            System.out.print(" "+i);
        }
    }
}
